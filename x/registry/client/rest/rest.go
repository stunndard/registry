package rest

import (
	"github.com/gorilla/mux"

	"github.com/cosmos/cosmos-sdk/client"
	// this line is used by starport scaffolding # 1
)

const (
	MethodGet = "GET"
)

// RegisterRoutes registers registry-related REST handlers to a router
func RegisterRoutes(clientCtx client.Context, r *mux.Router) {
	// this line is used by starport scaffolding # 2
	registerQueryRoutes(clientCtx, r)
	registerTxHandlers(clientCtx, r)

}

func registerQueryRoutes(clientCtx client.Context, r *mux.Router) {
	// this line is used by starport scaffolding # 3
	r.HandleFunc("/registry/names/{id}", getNameHandler(clientCtx)).Methods("GET")
	r.HandleFunc("/registry/names", listNameHandler(clientCtx)).Methods("GET")

}

func registerTxHandlers(clientCtx client.Context, r *mux.Router) {
	// this line is used by starport scaffolding # 4
	r.HandleFunc("/registry/names", buyNameHandler(clientCtx)).Methods("POST")
	r.HandleFunc("/registry/names/{id}", updateNameHandler(clientCtx)).Methods("POST")
	r.HandleFunc("/registry/names/{id}", deleteNameHandler(clientCtx)).Methods("POST")

}
